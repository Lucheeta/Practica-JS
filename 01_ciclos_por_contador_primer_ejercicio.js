//Realizá un programa que muestre todos los números enteros del 1 al 100, y luego, los mismos números, pero en orden inverso.

for (let contador = 1; contador <= 100; contador++) {
	console.log(`${contador}`);
}
console.log("/////////////////////////");

for (let contador = 100; contador >= 1; contador--) {
	console.log(`${contador}`);
}